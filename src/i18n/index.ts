const translations: Record<Locale, Translations> = { en, vi };

export function t(
  locale: Locale,
  key: keyof Translations,
  params?: Record<string, string | number>,
): string {
  const str = translations[locale][key] ?? translations.en[key];
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? ''));
}
